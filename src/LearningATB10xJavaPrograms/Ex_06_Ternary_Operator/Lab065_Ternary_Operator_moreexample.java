package Ex_06_Ternary_Operator;

public class Lab065_Ternary_Operator_moreexample {
    public static void main(String[] args) {
    //Find the maximum number between two numbers.
    //X and Y -> max X, y -> Ternary operator.
     int x = 10;
     int y = 20;
     int max = x > y ? x : y;  // if x is greater the output will be 10 else 20
     System.out.println(max);
}
}