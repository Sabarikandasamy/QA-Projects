package Ex_11_While;

public class Lab117_While_Loop {
    public static void main(String[] args) {
        // I, C, U (While loop)
        int i = 0; //i
         while (i < 10) { //IF need to enter into the loop the condition should be true
             System.out.println(i);
             i++; // U
         }

         // I, C, U (For loop)
        for (int j = 0; j < 10; j++) {
            System.out.println(j);
        }
    }
}
