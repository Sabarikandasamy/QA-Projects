package Ex_11_While;

public class Lab119_Example_While_for_loop {
    public static void main(String[] args) {
        int i = 0;
        while (i > 0) {
            // 0 > 0 :false (if the condition is not true it wont enter the loop)
            System.out.println("Enter");
            System.out.println(i);
            i++;
        }
    }
}
