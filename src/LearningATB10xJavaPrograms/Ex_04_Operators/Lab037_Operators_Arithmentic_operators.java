package Ex_04_Operators;

public class Lab037_Operators_Arithmentic_operators {
    public static void main(String[] args) {

        // Arithmetic operators
        /*  + (Addition)
            - (Subtraction)
            * (Multiplycation)
            / (Division)
            % (Modulous) - remainder
             */
        int a = 120;
        int b = 10;
        float c = 3f;

        System.out.println(a+b); // a and b are the operands
        System.out.println(a/b); // output is 6
        System.out.println(a/c); // output is 6.666

    }

}
