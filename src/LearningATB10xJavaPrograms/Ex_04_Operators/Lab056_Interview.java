package Ex_04_Operators;

public class Lab056_Interview {
    public static void main(String[] args) {
       short s = 10;
       char c = 'A';  //ASCII A = 65
        System.out.println(c+s); // Output : 65 + 10 = 75
        System.out.println(c);  // Output : 65

        // When you do the (+) concatination, then only ASCII values are used.
    }
}
