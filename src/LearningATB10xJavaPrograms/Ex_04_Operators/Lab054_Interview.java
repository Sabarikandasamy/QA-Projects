package Ex_04_Operators;

public class Lab054_Interview {
    public static void main(String[] args) {
        char a1 = 'A'; // It will take the ASCII value assigned  A--> 65
        char b1 = 'B'; // It will take the ASCII value assigned  B--> 66
        System.out.println(a1 +b1); //AB

         /* a1+b1 -65+66 -131
            Rule - concatenation of String -> char -> int calculation
             a1 + b1 - 65+66 -> 131 */
        // Output = 131
    }
}
