package Ex_04_Operators;

public class Lab040_Relational_Operators {
    public static void main(String[] args) {
        // < Less than
        // > = Greater than or equal to
        // All of them will result boolean output (True or False).
        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

    }
}
