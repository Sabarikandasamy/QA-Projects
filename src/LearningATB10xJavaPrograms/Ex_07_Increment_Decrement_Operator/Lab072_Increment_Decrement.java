package Ex_07_Increment_Decrement_Operator;

public class Lab072_Increment_Decrement {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);

        // Method
        // Line no | Exp(result) | a
        // 5 | NA | 10
        // 6 | 11 | 11

    }
}
