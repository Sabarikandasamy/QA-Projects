package Ex_07_Increment_Decrement_Operator;

public class Lab074_Advance_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a); // here a++ -> 10 ++ = 10 and + a -> 10 + 11 = 21 (Output)
     // A + B
        // A -> a++ -> Exp1(A) -> 10, a -> 11
        // +
        // B -> a -> a 11 -> Exp2 -> 11
        // A + B -> 10 + 11 -> 21



    }
}
