package Ex_07_Increment_Decrement_Operator;

public class Lab073_Increment_Decrement {
    public static void main(String[] args) {
        int a = 25;
        int x = 36;
        int i = ++x;
        int z = ++a;
        System.out.println(a);
        System.out.println(x);
        System.out.println(z);
        System.out.println(i);

    }
}
